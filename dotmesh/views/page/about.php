    <div class="content">
    	<h3>What is DotMesh?</h3>
    	<p>Dotmesh does not run on a central server. It is by its nature decentralized and crowd-sourced. It is the complete mesh of independent community members of the world that make up the Dotmesh network. It can never be wholly blocked or eliminated, because the global network of Dotmesh nodes are self-sustaining in the absense of one or many others.</p>
    	<p>It is exciting to see how Dotmesh will be adopted by the community. We understand that the original motivation of the creators does not determine its possible use, and all the creative ways in which it will be used by the community to enrich lives and promote communication can never be predicted.</p>
    	<h3>What prompted the creation of Dotmesh</h3>
    	<p>We watched the world stage as the undemocratic nations of the world abused their powers against their own citizens.</p>
    	<p>As recent world events unfolded, it became clear that the law-abiding, honest citizens of the world needed the tool necessary to communicate in privacy, without fear of persecution.</p>
    	<p>We believe that a tolerating world that promotes peace and freedom of opinion and expression, is the highest aspiration of the common people. Dotmesh is our small contribution in hopes of a tolerant, free world.</p>
